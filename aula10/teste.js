// Aula 10.2: Uso do parseInt() e Tipos de Variáveis

console.log('Seja bem-vindo!');

const idade = parseInt('23'); // Usando 'parseInt()' para converter a string '23' em um número inteiro.
const anoNascimento = parseInt('1999'); // Usando 'parseInt()' para converter a string '1999' em um número inteiro.

console.log(typeof idade); // Isso exibirá 'number' no console, indicando que 'idade' é um número inteiro.
console.log(typeof anoNascimento); // Isso também exibirá 'number', pois 'anoNascimento' é um número inteiro.
