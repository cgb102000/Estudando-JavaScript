// Aula 04: Variáveis em JavaScript

// Em JavaScript, você pode usar variáveis para armazenar dados. Veja um exemplo:

let nome = 'Caio'; // Aqui criamos uma variável chamada 'nome' e atribuímos o valor 'Caio' a ela.

// Você pode usar 'console.log()' para exibir informações no console do navegador.

console.log(nome, 'Nasceu em 1999'); // Isso exibirá 'Caio Nasceu em 1999' no console.

console.log('Em 2016', nome, 'conheceu Brenda'); // Isso exibirá 'Em 2016 Caio conheceu Brenda' no console.

console.log(nome, 'e Brenda não tiveram filhos'); // Isso exibirá 'Caio e Brenda não tiveram filhos' no console.
