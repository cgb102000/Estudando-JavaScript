// Aula 03: Usando console.log() e alert() em JavaScript

// Em JavaScript, você pode usar 'console.log()' e '.alert()' para exibir informações ou mensagens.

// O 'console.log()' é usado para exibir mensagens no console do navegador. É uma ferramenta útil para depuração e registro de informações.

console.log('oi'); // Isso exibirá 'oi' no console do navegador.

// .alert() é usado para exibir uma caixa de diálogo de alerta no navegador com a mensagem especificada.

window.alert('oi'); // Isso exibirá uma caixa de diálogo de alerta com a mensagem 'oi'.

// 'console.log()' é frequentemente usado para registrar informações para fins de depuração, enquanto '.alert()' é usado para interagir com o usuário exibindo uma mensagem pop-up.

// Lembre-se de que '.alert()' interromperá a execução do código até que o usuário clique em "OK" na caixa de diálogo de alerta.
