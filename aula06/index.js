// Aula 06: Tipos de Variáveis em JavaScript e Operações

// Em JavaScript, você pode usar diferentes tipos de variáveis, como 'const' e 'let', para armazenar dados. Também pode realizar operações com essas variáveis.

const primeiroNumero = '5'; // Aqui, 'primeiroNumero' é uma constante que armazena uma string '5'.
const segundoNumero = 20; // 'segundoNumero' é uma constante que armazena um número 20.

const resultado = primeiroNumero * segundoNumero; // Realizamos uma operação de multiplicação entre 'primeiroNumero' e 'segundoNumero'.

let resultadoTriplicado = resultado * 3; // Multiplicamos 'resultado' por 3 e armazenamos em 'resultadoTriplicado'.

resultadoTriplicado = resultadoTriplicado + 5; // Adicionamos 5 a 'resultadoTriplicado'.

// Você pode usar 'console.log()' para exibir informações no console do navegador. 'typeof()' é uma função que mostra o tipo de variável.

console.log(typeof (primeiroNumero + segundoNumero)); // Isso exibirá 'string' no console porque 'primeiroNumero' é uma string e 'segundoNumero' é um número.
