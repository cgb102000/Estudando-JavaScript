// Aula 05: Declaração de Variáveis em JavaScript

// Em JavaScript, você pode declarar variáveis usando a palavra-chave 'let' seguida pelo nome da variável.

let nomeCliente = 'Caio'; // Aqui criamos uma variável chamada 'nomeCliente' e atribuímos o valor 'Caio' a ela.

// Você pode usar 'console.log()' para exibir o valor da variável no console do navegador.

console.log(nomeCliente); // Isso exibirá 'Caio' no console.

// As variáveis permitem armazenar e manipular dados em seu código JavaScript.

// Lembre-se de que é importante escolher nomes significativos para suas variáveis para tornar o código mais legível e compreensível.
